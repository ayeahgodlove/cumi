export const TranslationProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [locale, setLocale] = useState<string>('en');

  useEffect(() => {
    // Get locale from cookie or default to 'en'
    const savedLocale = document.cookie
      .split('; ')
      .find(row => row.startsWith('NEXT_LOCALE='))
      ?.split('=')[1] || 'en';
    setLocale(savedLocale);
  }, []);

  const handleLocaleChange = (newLocale: string) => {
    setLocale(newLocale);
    // Save to cookie
    document.cookie = `NEXT_LOCALE=${newLocale}; path=/; max-age=31536000`;
    // Reload page to apply translations
    window.location.reload();
  };

  const t = (key: string, params?: Record<string, string | number>): string => {
    let translation = translations[locale as keyof typeof translations]?.[key as keyof typeof translations['en']] || key;
    
    // Replace parameters in the translation string
    if (params) {
      Object.entries(params).forEach(([paramKey, paramValue]) => {
        translation = translation.replace(`{${paramKey}}`, String(paramValue));
      });
    }
    
    return translation;
  };

  return (
    <TranslationContext.Provider value={{ locale, setLocale: handleLocaleChange, t }}>
      {children}
    </TranslationContext.Provider>
  );
};

export const useTranslation = () => {
  const context = useContext(TranslationContext);
  if (context === undefined) {
    throw new Error('useTranslation must be used within a TranslationProvider');
  }
  return context;
};

// Language selector component
export const LanguageSelector: React.FC = () => {
  const { locale, setLocale, t } = useTranslation();

  const languageItems = [
    {
      key: 'en',
      label: (
        <Space>
          <span>🇺🇸</span>
          <span>English</span>
          {locale === 'en' && <span style={{ color: '#1890ff' }}>✓</span>}
        </Space>
      ),
      onClick: () => setLocale('en'),
    },
    {
      key: 'fr',
      label: (
        <Space>
          <span>🇫🇷</span>
          <span>Français</span>
          {locale === 'fr' && <span style={{ color: '#1890ff' }}>✓</span>}
        </Space>
      ),
      onClick: () => setLocale('fr'),
    },
  ];

  return (
    <Dropdown
      menu={{ items: languageItems }}
      placement="bottomRight"
      arrow
    >
      <Button
        type="text"
        icon={<GlobalOutlined />}
        style={{
          display: 'flex',
          alignItems: 'center',
          gap: '4px',
          color: '#666',
        }}
      >
        {locale === 'en' ? '🇺🇸' : '🇫🇷'}
      </Button>
    </Dropdown>
  );
};

